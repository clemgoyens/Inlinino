

def sizeof_fmt(num, suffix='B'):
    if abs(num) < 1024:
        return "%3d %s" % (num, suffix)
    for unit in ['', 'K', 'M', 'G', 'T', 'P', 'E', 'Z']:
        if abs(num) < 1024.0:
            return "%3.1f %s%s" % (num, unit, suffix)
        num /= 1024.0
    return "%.1f %s%s" % (num, 'Y', suffix)
